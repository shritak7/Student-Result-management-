    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollNo=jTextField1.getText();
        String physics=jTextField1.getText();
        String maths=jTextField1.getText();
        String em=jTextField1.getText();
        String dbms=jTextField1.getText();
        String os=jTextField1.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Parbhani@123");
            Statement st = con.createStatement();
            
            ResultSet rs=st.executeQuery("Select * from student where rollno='"+rollNo+"'");
            if(rs.next()){
             st.executeUpdate("insert into result(rollno,physics,maths,em,dbms,os) values('"+rollNo+"','"+physics+"','"+maths+"','"+em+"','"+dbms+"','"+os+"')");   
            JOptionPane.showMessageDialog(null, "Marks saved successfully !");
            setVisible(false);
            new addResult().setVisible(true);
            }
            else{
               JOptionPane.showMessageDialog(null, "This roll number is registered !"); 
            }
           
            
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

